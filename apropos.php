        <h2 align="center" >Direction Gérénale des Recettes de Kongo Central(DGRKC)</h2>
        <h3 style="padding: 10px">Présentation</h3>

        <div class="w5">
        <div style="padding: 10px" class="row-1" align="center">
        <img src="images/dgrkc.jpg" width="1000px" height="500px">
        </div>
        <div style="padding: 10px" class="row-1" name="">
            <p>Créée en remplacement de l’ancienne Régie Provinciale d’Encadrement et de Recouvrement des Recettes de la Province, suivant Arrêté Provincial n°090/BIS/CAB.GOUV/KC/001/2017 modifiant et complétant l'Arrêté Provincial n°090/BIS/CAB.GOUV/KC/087/2015 du 26 novembre 2015, la Direction Générale des recettes du Kongo Central, DGR/KC en sigle, est une structure chargée de la mobilisation des recettes qui permettent à l’Exécutif Provincial de financer les projets de développement mis en place. Celle-ci prend en charge l'actif mobilier et immobilier ainsi que le passif financier direct de l'ex-REPERE.</p>

            <p>Elle est placée sous l'Autorité du Ministre Provincial ayant les Finances dans ses attributions. Elle a son siège à Matadi, Chef lieu de la Province du Kongo Central mais celui-ci peut être déplacé, en cas de nécessité, dans tout autre endroit de la Province sur décision du Gouvernement Provincial.</p>

            <p>Ces recettes découlent des actes générateurs repris dans l’Ordonnance-loi n°001/13 du 31 décembre 2013 fixant la nomenclature des impôts, taxes et redevances provinciaux ainsi que leurs modalités de répartition, corroborée par l’Edit n°005 du 23 février 2013 portant classification par service d’assiette, des impôts, taxes et redevances de la Province du Kongo Central.</p>
        </div>
    </div>

        <h3 style="padding: 10px">Mission de la DGRKC</h3>
        <p style="padding: 10px">
            La Direction Générale des recettes du Kongo Central "DGR/KC" a pour mission essentielle: la mobilisation des recettes provinciales relatives aux Impôts, Taxes, Droits et Redevances d'intérêt commun ainsi que des Taxes spécifiques en vue de permettre à l'Exécutif Provincial d'assurer la politique de ses moyens.


            La DGR/KC vient en appui aux Entités Territoriales Decentralisées, à la requête de celles-ci, dans le cadre de la mobilisation de leurs recettes locales. 

            La Direction Générale des recettes du Kongo Central, DGR/KC en sigle, exerce, conformément  aux lois et règlements en vigueur, toutes les missions et prérogatives concernant  notamment :

            L’assiette, le recouvrement, le contrôle et le contentieux des impôts provinciaux ;

            L’ordonnancement, le recouvrement, le contrôle, et le contentieux des droits, taxes et redevances provinciaux ;

            La gestion des imprimés de valeur.       
        </p>
 

<p style="padding: 10px">Dans le cadre de ses missions :</p>

<p style="padding: 10px">Elle est chargée de l’encadrement des recettes fiscales et non fiscales perçues par les régies financières nationales afin de s’assurer que la quotité des recettes à retenir à la source par la province est assisse sur une basse correcte ;

Et en collaboration avec les autres administrations, elle peut élaborer et soumettre aux autorités compétentes des projets d’édit, d’arrêté, de circulation et autres instructions dans le domaine de ses attributions ;

Elle est consultée pour toute modification ou révisions de la législation et de la réglementation en matière de recettes fiscales, parafiscales, domaniales et de participations provinciales initiées par les autres administrations ainsi que pour toutes décisions d’admission au régime d’exception ;

Elle participe, enfin, à toutes les séances de collaboration des comptes auprès des règles d’Etat d’abord et ensuite à la Banque Centrale du Congo et les banques commerciales.</p>                                 

 
<p style="padding: 10px">En résumé, elle est chargée de la mobilisation des recettes relatives aux Impôts, taxes, droits et redevances d’intérêt commun ainsi que des taxes spécifiques de la Province.

Elle vient en appui aux Entités Territoriales Décentralisées, à la requête de celles-ci, dans le cadre de la mobilisation de leurs recettes locales.

Elle exerce ses missions sur toute l’étendue de la province du Kongo Central et est répartie dans les différents territoires.</p>

